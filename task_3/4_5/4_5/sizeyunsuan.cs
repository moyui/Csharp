    class sizeyunsuan
    {
        public double x;
        public double y;
        public double plus() 
        {
            return this.x + this.y;
        }
        public double minus() 
        {
            return this.x - this.y;
        }
        public double muti()
        {
            return this.x * this.y;
        }
        public double divid()
        {
            return this.x / this.y;
        }
    }
